    public void guardarProducto() {
        if (validacion.validarProducto(productoForm.gestion, cmbCategoria.getSelectedItem().toString(),
                txtCodigo.getText(), txtNombre.getText(),
                txtPrecio.getText(), txtStock.getText(), txtMarca.getText(), txtTalla.getText(), txtColor.getText(),
                cmbTamanio.getSelectedItem().toString(), txtMaterial.getText(), txtFragancia.getText(),
                txtDia.getText(), cmbMes.getSelectedItem().toString(), cmbAnio.getSelectedItem().toString())) {
            return;
        }
        String categoria = cmbCategoria.getSelectedItem().toString();
        String nombre = txtNombre.getText();
        String codigo = txtCodigo.getText();
        double dPrecio = Double.parseDouble(txtPrecio.getText());
        int iStock = Integer.parseInt(txtStock.getText());
        String marca = txtMarca.getText();
        int talla = -1;
        String material = txtMaterial.getText();
        String estacion = txtEstación.getText();
        if ("Accesorio".equals(categoria) || "Ropa".equals(categoria)) {
            talla = Integer.parseInt(txtTalla.getText());
        }

        String color = txtColor.getText();
        String tamanio = cmbTamanio.getSelectedItem().toString();
        String fragancia = txtFragancia.getText();
        String dia = txtDia.getText();
        String mes = cmbMes.getSelectedItem().toString();
        String anio = cmbAnio.getSelectedItem().toString();

        try {
            productoForm.gestion.ingresar(categoria, codigo, nombre, dPrecio, iStock, marca, talla, color, tamanio, material, fragancia, dia, mes, anio, estacion);
            productoForm.refrescar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

