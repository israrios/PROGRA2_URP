    private void cmbCategoriaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbCategoriaItemStateChanged
        if ("Alimento".equals(cmbCategoria.getSelectedItem())) {
            //ACTIVANDO DE ALIMENTOS
            txtMarca.setEnabled(true);
            txtDia.setEnabled(true);
            cmbMes.setEnabled(true);
            cmbAnio.setEnabled(true);
            //DESACTIVANDO OTROS
            txtTalla.setEnabled(false);
            cmbTamanio.setEnabled(false);
            txtMaterial.setEnabled(false);
            txtColor.setEnabled(false);
            txtFragancia.setEnabled(false);
            txtEstación.setEnabled(false);
        }
        if ("Ropa".equals(cmbCategoria.getSelectedItem())) {
            //ACTIVANDO DE ROPA
            txtTalla.setEnabled(true);
            txtMaterial.setEnabled(true);
            txtEstación.setEnabled(true);
            txtColor.setEnabled(true);
            //DESACTIVANDO OTROS
            txtMarca.setEnabled(false);
            cmbTamanio.setEnabled(false);
            txtFragancia.setEnabled(false);
            txtDia.setEnabled(false);
            cmbMes.setEnabled(false);
            cmbAnio.setEnabled(false);

        }
        if ("Accesorio".equals(cmbCategoria.getSelectedItem())) {
            //ACTIVANDO DE ACCESORIO
            txtTalla.setEnabled(true);
            txtMaterial.setEnabled(true);
            txtColor.setEnabled(true);
            //DESACTIVANDO OTROS
            txtMarca.setEnabled(false);
            cmbTamanio.setEnabled(false);
            txtFragancia.setEnabled(false);
            txtDia.setEnabled(false);
            cmbMes.setEnabled(false);
            cmbAnio.setEnabled(false);
            txtEstación.setEnabled(false);
        }
        if ("Aseo".equals(cmbCategoria.getSelectedItem())) {
            //ACTIVANDO DE ASEO
            txtFragancia.setEnabled(true);
            txtDia.setEnabled(true);
            cmbMes.setEnabled(true);
            cmbAnio.setEnabled(true);
            // DESACTIVANDO OTROS
            txtMarca.setEnabled(false);
            txtTalla.setEnabled(false);
            cmbTamanio.setEnabled(false);
            txtMaterial.setEnabled(false);
            txtColor.setEnabled(false);
            txtEstación.setEnabled(false);
        }
        if ("Juguete".equals(cmbCategoria.getSelectedItem())) {
            //ACTIVANDO DE JUGUETE
            cmbTamanio.setEnabled(true);
            txtMaterial.setEnabled(true);
            // DESACTIVANDO OTROS
            txtMarca.setEnabled(false);
            txtTalla.setEnabled(false);
            txtColor.setEnabled(false);
            txtFragancia.setEnabled(false);
            txtDia.setEnabled(false);
            cmbMes.setEnabled(false);
            cmbAnio.setEnabled(false);
            txtEstación.setEnabled(false);
        }
    }//GEN-LAST:event_cmbCategoriaItemStateChanged

